package com.roy.football.match;

public class RecentData {
	
	public Integer getGoals() {
		return goals;
	}
	public void setGoals(Integer goals) {
		this.goals = goals;
	}
	public Integer getLoses() {
		return loses;
	}
	public void setLoses(Integer loses) {
		this.loses = loses;
	}
	public Integer getWins() {
		return wins;
	}
	public void setWins(Integer wins) {
		this.wins = wins;
	}
	public Integer getDraws() {
		return draws;
	}
	public void setDraws(Integer draws) {
		this.draws = draws;
	}
	public Integer getLosts() {
		return losts;
	}
	public void setLosts(Integer losts) {
		this.losts = losts;
	}
	public Integer getWinPan() {
		return winPan;
	}
	public void setWinPan(Integer winPan) {
		this.winPan = winPan;
	}
	public Integer getDrawPan() {
		return drawPan;
	}
	public void setDrawPan(Integer drawPan) {
		this.drawPan = drawPan;
	}
	public Integer getLosePan() {
		return losePan;
	}
	public void setLosePan(Integer losePan) {
		this.losePan = losePan;
	}
	public Float getPoint() {
		return point;
	}
	public void setPoint(Float point) {
		this.point = point;
	}
	private Integer goals;
	private Integer loses;
	private Integer wins;
	private Integer draws;
	private Integer losts;
	private Integer winPan;
	private Integer drawPan;
	private Integer losePan;
	private Float point;
}
